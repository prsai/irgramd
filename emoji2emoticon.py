emo_inv = {}
for k in reversed(emo):
  emo_inv[emo[k][1:-1]] = k

def replace_mult(line, emo):
  for utf_emo in emo:
    if utf_emo in line:
      line = line.replace(utf_emo, emo[utf_emo])
  return line
